# –ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è –≤—ã–≤–æ–¥–∏—Ç —Å–ª–æ–≤–æ, –∫–æ—Ç–æ—Ä–æ–µ –≤—Å—Ç—Ä–µ—á–∞–µ—Ç—Å—è —Ä–µ–∂–µ –≤—Å–µ–≥–æ, –±–µ–∑ —É—á–µ—Ç–∞ —Ä–µ–≥–∏—Å—Ç—Ä–∞. 
# –ï—Å–ª–∏ —Ç–∞–∫–∏—Ö —Å–ª–æ–≤ –Ω–µ—Å–∫–æ–ª—å–∫–æ, –≤—ã–≤–µ–¥–∏—Ç–µ —Ç–æ, –∫–æ—Ç–æ—Ä–æ–µ –º–µ–Ω—å—à–µ –≤ –ª–µ–∫—Å–∏–∫–æ–≥—Ä–∞—Ñ–∏—á–µ—Å–∫–æ–º –ø–æ—Ä—è–¥–∫–µ.

u_string = input().lower().split()
signs = ".,!?:;- "
clear_str = []
for word in u_string:
    if len(word)>1 and word[-1] in signs:
        word = word[:-1]
        clear_str.append(word)
    elif len(word) == 1 and word in signs:
        continue
    else:
        clear_str.append(word)
result = {}

# –ü–æ–¥—Å—á–µ—Ç —á–∞—Å—Ç–æ—Ç—ã —Å–ª–æ–≤
for word in clear_str:
    if word not in result:
        result[word] = clear_str.count(word)

# –ù–∞—Ö–æ–¥–∏–º –º–∏–Ω–∏–º–∞–ª—å–Ω—É—é —á–∞—Å—Ç–æ—Ç—É
min_freq = min(result.values())

# –ù–∞—Ö–æ–¥–∏–º –ª–µ–∫—Å–∏–∫–æ–≥—Ä–∞—Ñ–∏—á–µ—Å–∫–∏ –º–∏–Ω–∏–º–∞–ª—å–Ω–æ–µ —Å–ª–æ–≤–æ —Å—Ä–µ–¥–∏ —Å–ª–æ–≤ —Å –º–∏–Ω–∏–º–∞–ª—å–Ω–æ–π —á–∞—Å—Ç–æ—Ç–æ–π
min_word = min([word for word, freq in result.items() if freq == min_freq])

print(min_word)