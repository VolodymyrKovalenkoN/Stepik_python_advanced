# Считываем выборы Тимура и Руслана
timur = input().lower()  # Переводим в нижний регистр для единообразия
ruslan = input().lower()

# Определяем победителя
if timur == ruslan:
    print("ничья")
else:
    # Создаем словарь, который определяет, что побеждает каждый элемент
    # Ключ побеждает все значения в соответствующем списке
    wins = {
        "камень": ["ножницы", "ящерица"],
        "ножницы": ["бумага", "ящерица"],
        "бумага": ["камень", "спок"],
        "ящерица": ["бумага", "спок"],
        "спок": ["камень", "ножницы"]
    }
    
    # Проверяем, побеждает ли выбор Тимура выбор Руслана
    if ruslan in wins[timur]:
        print("Тимур")
    else:
        print("Руслан")